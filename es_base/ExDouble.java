package es_base;

public class ExDouble {
    public static void main(String[] args) {

        int v = 10;
        double x = 10.0;

        System.out.println("Prima della divisione\nV = [" + v + "]\nX = [" + x + "]");
        v = v / 4;
        x = x / 4;
        System.out.println("=================");
        System.out.println("Dopo la divisione\nV = [" + v + "]\nX = [" + x + "]");
    }

}